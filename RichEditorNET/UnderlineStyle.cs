namespace EllipticBit.RichEditorNET
{
	/// <summary>
	/// Specifies the underline style to apply. Values correspond to HTML5/CSS text-decoration-style options.
	/// </summary>
	public enum UnderlineStyle
	{
		/// <summary>A single solid underline (CSS: solid).</summary>
		Single = 1,
		/// <summary>A double underline (CSS: double).</summary>
		Double = 3,
		/// <summary>A dotted underline (CSS: dotted).</summary>
		Dotted = 4,
		/// <summary>A dashed underline (CSS: dashed).</summary>
		Dashed = 5,
		/// <summary>A wavy underline (CSS: wavy).</summary>
		Wavy = 8,
	}
}
